package com.anchy.po;

import java.io.Serializable;

public class Cart implements Serializable {
	private int cid;
	private User user;
	private Type type;
	private int ctid;
	private Goods goods;
	private int cartsum;
	public Cart() {
		super();
	}
	
	public Cart(int cid, User user, Type type, int ctid, Goods goods,
			int cartsum) {
		super();
		this.cid = cid;
		this.user = user;
		this.type = type;
		this.ctid = ctid;
		this.goods = goods;
		this.cartsum = cartsum;
	}

	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public User getUser() {
		return user;
	}
	public void setUser(User user) {
		this.user = user;
	}
	public Type getType() {
		return type;
	}
	public void setType(Type type) {
		this.type = type;
	}
	public int getCtid() {
		return ctid;
	}
	public void setCtid(int ctid) {
		this.ctid = ctid;
	}

	public Goods getGoods() {
		return goods;
	}

	public void setGoods(Goods goods) {
		this.goods = goods;
	}

	public int getCartsum() {
		return cartsum;
	}

	public void setCartsum(int cartsum) {
		this.cartsum = cartsum;
	}

	
	
}
