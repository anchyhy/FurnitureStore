package com.anchy.po;

import java.io.Serializable;

public class Order implements Serializable {
	private int oid;
	private Type type;
	private int otid;
	private User user;
	private int ouid;
	private int flag;
	private Address address;
	private int oaid;
	public String message;
	private Goods goods;
	private int quantity;
	public Order() {
		super();
	}
	
	public Order(int oid, Type type, int otid, User user, int ouid, int flag,
			Address address, int oaid, String message,Goods goods,int quantity) {
		super();
		this.oid = oid;
		this.type = type;
		this.otid = otid;
		this.user = user;
		this.ouid = ouid;
		this.flag = flag;
		this.address = address;
		this.oaid = oaid;
		this.message = message;
		this.goods=goods;
		this.quantity=quantity;
	}

	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	public int getOid() {
		return oid;
	}
	public void setOid(int oid) {
		this.oid = oid;
	}
	public Type getType() {
		return type;
	}
	public void setType(Type type) {
		this.type = type;
	}
	public User getUser() {
		return user;
	}
	public void setUser(User user) {
		this.user = user;
	}
	public int getFlag() {
		return flag;
	}
	public void setFlag(int flag) {
		this.flag = flag;
	}
	public Address getAddress() {
		return address;
	}
	public void setAddress(Address address) {
		this.address = address;
	}

	public int getOtid() {
		return otid;
	}

	public void setOtid(int otid) {
		this.otid = otid;
	}

	public int getOuid() {
		return ouid;
	}

	public void setOuid(int ouid) {
		this.ouid = ouid;
	}

	public int getOaid() {
		return oaid;
	}

	public void setOaid(int oaid) {
		this.oaid = oaid;
	}

	public Goods getGoods() {
		return goods;
	}

	public void setGoods(Goods goods) {
		this.goods = goods;
	}

	public int getQuantity() {
		return quantity;
	}

	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}
}
