package com.anchy.po;

public class Material {
	private int mid;
	private String name;
	public Material() {
		super();
	}
	public Material(int mid, String name) {
		super();
		this.mid = mid;
		this.name = name;
	}
	public int getMid() {
		return mid;
	}
	public void setMid(int mid) {
		this.mid = mid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
