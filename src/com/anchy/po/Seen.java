package com.anchy.po;

public class Seen {
	private int sid;
	private User user;
	private Goods goods;
	public Seen() {
		super();
	}
	public Seen(int sid, User user, Goods goods) {
		super();
		this.sid = sid;
		this.user = user;
		this.goods = goods;
	}
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public User getUser() {
		return user;
	}
	public void setUser(User user) {
		this.user = user;
	}
	public Goods getGoods() {
		return goods;
	}
	public void setGoods(Goods goods) {
		this.goods = goods;
	}
	
}
