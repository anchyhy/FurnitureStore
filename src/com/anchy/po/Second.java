package com.anchy.po;

import java.io.Serializable;

public class Second implements Serializable {
	private int sid;
	private String name;
	private First first;
	public Second() {
		super();
	}
	public Second(int sid, String name, First first) {
		super();
		this.sid = sid;
		this.name = name;
		this.first = first;
	}
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public First getFirst() {
		return first;
	}
	public void setFirst(First first) {
		this.first = first;
	}
	
	
}
